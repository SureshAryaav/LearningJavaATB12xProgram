package ex_03_Operators;

public class Lab013_AssignmentOperator {
    public static void main(String[] args) {
                int a = 5;
                a += 5;  // a = a + 5
                a -= 3;  // a = a - 3
                a *= 2;  // a = a * 2
                a /= 4;  // a = a / 4
                a %= 3;  // a = a % 3
                System.out.println("Final value: " + a);  // 1
    }
}

