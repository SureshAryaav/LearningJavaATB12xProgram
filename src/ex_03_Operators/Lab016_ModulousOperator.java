package ex_03_Operators;

public class Lab016_ModulousOperator {
        public static void main(String[] args) {
            int a = 10;
            int b = 3;

            int result = a % b;  // 10 divided by 3 gives 3 remainder 1
            System.out.println("10 % 3 = " + result);  // Output: 1
        }


}
